// Function to display students with the updated format
function displayStudents(data) {
  const studentList = document.getElementById('studentList');
  studentList.innerHTML = ''; // Clear previous content

  data.forEach(student => {
      const studentCard = document.createElement('div');
      studentCard.classList.add('student-card');
      studentCard.innerHTML = `
          <img src="${student.image}" alt="${student.name}">
          <h3>${student.name}</h3>
          <p>Roll: ${student.roll}</p>
          <p>Registration: ${student.registration}</p>
          <p>Semester: ${student.semester}</p>
          <p>Shift: ${student.shift}</p>
          <p>Season: ${student.season}</p>
          <p>Address: ${student.address}</p>
          <p>Email: ${student.email}</p>
          <p>Mobile: ${student.mobile}</p>
      `;
      studentList.appendChild(studentCard);
  });
}

// Initial display of all students
displayStudents(students);

// Search and filter functionality
const searchInput = document.getElementById('searchInput');
const shiftFilter = document.getElementById('shiftFilter');

function filterStudents() {
  const searchTerm = searchInput.value.toLowerCase();
  const selectedShift = shiftFilter.value;

  const filteredStudents = students.filter(student => {
      const matchesSearch = student.name.toLowerCase().includes(searchTerm) || student.roll.includes(searchTerm);
      const matchesShift = selectedShift ? student.shift === selectedShift : true;
      return matchesSearch && matchesShift;
  });

  displayStudents(filteredStudents);
}

// Event listeners for search and filter
searchInput.addEventListener('input', filterStudents);
shiftFilter.addEventListener('change', filterStudents);

let slideIndex = 0;
showSlides();

function showSlides() {
  const slides = document.getElementsByClassName("slide");
  for (let i = 0; i < slides.length; i++) {
      slides[i].style.display = "none";
  }
  slideIndex++;
  if (slideIndex > slides.length) { slideIndex = 1 }
  slides[slideIndex - 1].style.display = "block";
  setTimeout(showSlides, 3000); // Change slide every 3 seconds
}

